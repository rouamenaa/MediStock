package com.esprit.microservice.orderservice.dto;

import java.math.BigDecimal;
import java.util.List;

public class OrderDocumentGenerationRequestDto {
    private Long orderId;
    private String orderNumber;
    private String patientId;
    private String patientName;
    private Long pharmacyId;
    private String status;
    private BigDecimal totalAmount;
    private String createdAt;
    private List<OrderDocumentLineDto> items;

    public Long getOrderId() {
        return orderId;
    }

    public void setOrderId(Long orderId) {
        this.orderId = orderId;
    }

    public String getOrderNumber() {
        return orderNumber;
    }

    public void setOrderNumber(String orderNumber) {
        this.orderNumber = orderNumber;
    }

    public String getPatientId() {
        return patientId;
    }

    public void setPatientId(String patientId) {
        this.patientId = patientId;
    }

    public String getPatientName() {
        return patientName;
    }

    public void setPatientName(String patientName) {
        this.patientName = patientName;
    }

    public Long getPharmacyId() {
        return pharmacyId;
    }

    public void setPharmacyId(Long pharmacyId) {
        this.pharmacyId = pharmacyId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public BigDecimal getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(BigDecimal totalAmount) {
        this.totalAmount = totalAmount;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public List<OrderDocumentLineDto> getItems() {
        return items;
    }

    public void setItems(List<OrderDocumentLineDto> items) {
        this.items = items;
    }
}
